</section>		<section class="section definition-section" data-menu-title="Definition" id="content-body">
<p>A learning management system (LMS) is a software application or web-based technology used to plan, implement and assess a specific learning process. It is used for <a href="https://www.techtarget.com/whatis/definition/distance-learning-e-learning">eLearning</a> practices and, in its most common form, consists of two elements: a server that performs the base functionality and a user interface that is operated by instructors, students and administrators.</p> 
<p>Typically, a learning management system provides an instructor with a way to create and deliver content, monitor student participation and assess student performance. A learning management system may also provide students with the ability to use interactive features such as threaded discussions, <a href="https://www.techtarget.com/searchunifiedcommunications/definition/video-conference">video conferencing</a> and discussion forums.</p> 
<p>LMSes are frequently used by businesses of all sizes, national government agencies, local governments, traditional educational institutions and online/eLearning-based institutions. The systems can improve traditional educational methods, while also saving organizations time and money. An effective system will allow instructors and administrators to efficiently manage elements such as user registration, content, calendars, user access, communication, certifications and notifications.</p> 
<p>The Advanced Distance Learning group, sponsored by the United States Department of Defense, has created a set of specifications called Shareable Content Object Reference Model (SCORM) to encourage the standardization of learning management systems.</p> 
<section class="section main-article-chapter" data-menu-title="What are learning management systems used for?">
 <h3 class="section-title"><i class="icon" data-icon="1"></i>What are learning management systems used for?</h3>
 <p>LMSes are beneficial to a wide range of organizations, including higher education institutions and corporations. The primary use of a learning management system is for knowledge management (<a href="https://searchdomino.techtarget.com/definition/knowledge-management">KM</a>). KM refers to the gathering, organizing, sharing and analysis of an organization's knowledge in terms of resources, documents and people skills. However, the specific role of the LMS will vary according to the organization's training strategy and goals.</p>
 <p>Some popular LMSes used by educational institutions include Moodle, Blackboard Learn and Schoology. Popular enterprise-level LMSes include Adobe Captivate Prime, Docebo LMS, TalentLMS, iSpring Learn and eFront.</p>
 <p>Employee training and <a href="https://www.techtarget.com/searchhrsoftware/definition/employee-onboarding-and-offboarding">onboarding</a> is one of the most common use cases for an LMS in a corporate environment. In this case, the LMS is used to help train new employees by providing opportunities to access training materials across various devices. New employees can be recognized when they add their own knowledge and feedback, which will, in turn, help employers understand how effective the training courses are and identify areas where new employees need more assistance.</p>
 <p>LMSes can be used for extended enterprise training purposes as well. This includes customer, partner and member training. Customer training is common in software and technology companies where users need to be taught how a system works before they can use the new product. Providing ongoing customer training will also help improve customer experience and increase <a href="https://www.techtarget.com/searchcustomerexperience/definition/brand-loyalty">brand loyalty</a>.</p>
 <p>Another common use of LMSes in corporate environments is for employee development and <a href="https://www.techtarget.com/searchhrsoftware/definition/employee-retention">retention</a>. The LMS can be used to assign the necessary courses to current employees to ensure they are <a href="https://www.techtarget.com/searchhrsoftware/feature/10-LD-trends-for-the-next-normal">developing effective job skills</a>, remain informed about product changes and maintain relevant knowledge through new product and compliance training.</p>
</section>      
<section class="section main-article-chapter" data-menu-title="How do learning management systems work?">
 <h3 class="section-title"><i class="icon" data-icon="1"></i>How do learning management systems work?</h3>
 <p>A learning management system can be thought of as a large <a href="https://www.techtarget.com/searchoracle/definition/repository">repository</a> that allows users to store and track information in one place. Any user with a secure login and password can access the system and its online learning resources. Or, if the system is self-hosted, the user must either install the software on their <a href="https://www.techtarget.com/searchstorage/definition/hard-disk-drive">hard drive</a> or access it through their company's server.</p>
 <p>Some common features found in a successful LMS include:</p>
 <p><strong>Responsive design - </strong>Users should be able to access the LMS from whatever type of device they choose, whether it's a desktop, laptop, tablet or smartphone. The LMS should automatically display the version best suited for the user's chosen device. Additionally, the LMS should also allow users to download content so it is accessible while offline.</p>
 <p><strong>User-friendly interface - </strong>The user interface (<a href="https://www.techtarget.com/searchapparchitecture/definition/user-interface-UI">UI</a>) should enable learners to easily navigate the LMS platform. The UI should also align with the abilities and goals of both the user and the organization. An unintuitive UI risks confusing or distracting users and will make the LMS ineffective.</p>
 <p><strong>Reports and analytics - </strong>This includes eLearning assessment tools. Instructors and administrators must be able to view and track their online training initiatives to determine if they are effective or need adjusting. This can be applied to groups of learners and individuals.</p>
 <p><strong>Course and catalog management - </strong>The LMS holds all the eLearning courses and the related course content. Admins and instructors should be able to create and manage these catalogs and courses in order to deliver a more targeted learning experience.</p>
 <p><strong>Content interoperability and integration - </strong>Content created and stored in an LMS must be packaged in accordance with interoperable standards, including SCORM and <a href="https://www.techtarget.com/searchhrsoftware/definition/xAPI-experience-API">xAPI</a>.</p>
 <p><strong>Support services -</strong> Different LMS vendors offer varying levels of support. Many provide online <a href="https://www.techtarget.com/whatis/definition/discussion-board-discussion-group-message-board-online-forum">discussion boards</a> where users can connect and help each other. Additional support services, such as a dedicated toll-free service number, are available for an extra cost.</p>
 <p><strong>Certification and compliance support -</strong> This feature is essential to systems used for online <a href="https://www.techtarget.com/searchdatamanagement/definition/compliance">compliance</a> training and certifications. Instructors and admins should be able to assess an individual's skill set and identify any gaps in their performance. This feature will also make it possible to use LMS records during an audit.</p>
 <p><strong>Social learning capabilities - </strong>Many LMSes have started including social media tools within their platform. This allows users to interact with their peers, collaborate and share their learning experiences.</p>
 <p><strong>Gamification - </strong>Some LMSes include game mechanics or built-in <a href="https://www.techtarget.com/searchcustomerexperience/definition/gamification">gamification</a> features that allow instructors and admins to create courses with extra motivation and engagement. This can help students who need additional incentive to complete the course, possibly in the form of leaderboards, points and badges.</p>
 <p><strong>Automation - </strong>Learning management systems should enable administrators to <a href="https://www.techtarget.com/searchcio/definition/labor-automation">automate</a> repeated and tedious tasks. Examples include user grouping, new user population, user deactivation and group enrollments.</p>
 <p><strong>Localization - </strong>It is important for LMSes to include multilingual support features so the learning and training content can remain unaffected by language barriers. Some LMSes integrate <a href="https://www.techtarget.com/searchmobilecomputing/definition/geolocation">geolocation</a> features that allow them to automatically present the appropriate version of the course immediately upon access.</p>
 <p><strong>Artificial intelligence (AI) - </strong>Finally, <a href="https://www.techtarget.com/searchenterpriseai/definition/AI-Artificial-Intelligence">artificial intelligence</a> can help an LMS create personalized learning experiences for users by providing course formats suited to their needs, and by suggesting topics the user may find interesting based on the courses they have already completed.</p>
</section>               
<section class="section main-article-chapter" data-menu-title="Types of learning management systems">
 <h3 class="section-title"><i class="icon" data-icon="1"></i>Types of learning management systems</h3>
 <p>The different types of LMS deployment options are:</p>
 <ul class="default-list"> 
  <li>Cloud-based</li> 
  <li>Self-hosted</li> 
  <li>Desktop application</li> 
  <li>Mobile application</li> 
 </ul>
 <p><strong>Cloud-based LMSes</strong> are hosted on the cloud and often follow a <a href="https://www.techtarget.com/searchcloudcomputing/definition/Software-as-a-Service">software as a service</a> (SaaS) business model. Cloud-based LMS vendors take care of maintaining the system and performing any technical updates or upgrades. Online users can access the system from anywhere, at any time, using a username and password.</p>
 <p><strong>Self-hosted LMSes</strong> require software to be downloaded by the user. The self-hosted platform provides greater creative control and customization, but users must maintain the system themselves and often must pay for updates.</p>
 <p><strong>Desktop application LMSes</strong> are installed on the user's <a href="https://www.techtarget.com/searchenterprisedesktop/definition/desktop">desktop</a>. However, the application may still be accessible on multiple devices.</p>
 <p><strong>Mobile application LMSes</strong> support mobile learning and are accessible wherever and whenever through mobile devices. This platform deployment type allows users to engage with and track their online learning initiatives on the go.</p>
 <p>The various pricing models used for learning management systems include:</p>
 <ul class="default-list"> 
  <li>Freemium - This pricing model allows users to access the basic features of some LMS platforms. Once users start engaging with the more advanced functionalities of the system, then a fee is added.</li> 
  <li>Subscription - Users pay a recurring fee at regular intervals in order to access the LMS. The subscription may grant an organization total access to all LMS features, or it may require the organization to pay for each system user.</li> 
  <li>Licensing - This is either an annual fee that companies must renew or an upfront fee that provides users with unlimited lifetime access.</li> 
 </ul>
</section>         
<section class="section main-article-chapter" data-menu-title="Benefits of a learning management system">
 <h3 class="section-title"><i class="icon" data-icon="1"></i>Benefits of a learning management system</h3>
 <p>Learning management systems provide users with a variety of benefits, regardless of the type of organization using it. For example, an LMS can save an organization time and money. Instead of making learners take time out of their day to travel and sit through lessons or training, LMSes allow users to complete the coursework at a time that is best for them. Additionally, costs can be cut by eliminating the need for instructors, training days, training materials, travel expenses and location hiring.</p>
 <p>Other benefits of learning management systems include:</p>
 <ul class="default-list"> 
  <li>the ability to monitor user progress and performance;</li> 
  <li>increased eLearning accessibility without geographic limitations;</li> 
  <li>the ability to personalize the online training and learning experience;</li> 
  <li>the ability to easily and efficiently update eLearning modules and activities;</li> 
  <li>the ability to easily ensure online training and learning materials are being distributed effectively; and</li> 
  <li>the use of automation that allows users to forget about tedious, repetitive tasks -- such as user enrollment and certification distribution -- and focus on more important activities.</li> 
 </ul>
 <p>Finally, centralized learning is another major benefit provided by LMSes. It allows an organization to safely organize and store all <a href="https://www.techtarget.com/searchdatamanagement/definition/big-data">big data</a> in one location. This allows instructors and admins to more easily update and maintain learning materials. It also helps produce effective training that is consistent across the organization. Furthermore, most LMSes include advanced <a href="https://www.techtarget.com/searchsecurity/definition/encryption">encryption</a> features that help guarantee the data and content remain secure.</p>
</section>     
<section class="section main-article-chapter" data-menu-title="Examples of learning management systems">
 <h3 class="section-title"><i class="icon" data-icon="1"></i>Examples of learning management systems</h3>
 <p>As mentioned before, employee training and onboarding are some of the most common uses for LMSes. When using an LMS for these purposes, instructors can create immersive learning experiences that allow users to develop new skills and problem-solving capabilities. For example, an LMS could be used to create tutorials that incorporate <a href="https://www.techtarget.com/whatis/definition/augmented-reality-AR">augmented reality</a> (AR), virtual reality (VR) and even AI training. This will likely have the effect of improving creativity and innovation throughout the workforce.</p>
 <p>Another example of an LMS use case is for sales training. This can include onboarding and training, but also extends to include the creation of seminars on product knowledge, customer interaction training and case study-based tutorials that use previous experiences with clients to improve future interactions.</p>
 <p>An LMS can also be used to provide students with blended learning experiences. Blended learning combines traditional teaching in the classroom with online learning tools. This method is more effective than simple face-to-face education because it enriches the classroom-based experience with additional digital content that can be customized to fit a student's specific learning needs.</p>
